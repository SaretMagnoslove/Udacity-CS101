def print_abacus(value): 
    value = "0"*(10-len(str(value))) + str(value) 
    abacus_line = "00000*****" 
    for i in value: 
        digit = int(i) 
        print ("|" + abacus_line[:10-digit] + " " + abacus_line[10-digit:] + "|")

